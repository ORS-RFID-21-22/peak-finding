// First smooth the data.
// Each point is replaced with an average of n points (moving average)
// n must be odd (symmetrical including current point) to preserve relative peaks along the x-axis
// See an explanation of this type of smoothing here: https://terpconnect.umd.edu/~toh/spectrum/Smoothing.html
// smoothes iter times
function smoothTriangular(arr, n, iter) {
    var smoothed = [];
    for (var it = 0; it < iter; it++) {
        for (var i = 0; i < arr.length; i++) {
            var maxFactor = ((n + 1) / 2);
            var div = maxFactor;
            smoothed[i] = arr[i] * maxFactor;
            for (var j = 1; j < ((n + 1) / 2); j++) {
                if ((i - j) < 0) {
                    smoothed[i] += arr[i + j] * (maxFactor - j);
                    div += (maxFactor - j);
                } else if ((i + j) >= arr.length) {
                    smoothed[i] += arr[i - j] * (maxFactor - j);
                    div += (maxFactor - j);
                } else {
                    smoothed[i] += arr[i - j] * (maxFactor - j) + arr[i + j] * (maxFactor - j);
                    div += (maxFactor - j) * 2;
                }    
            }
            smoothed[i] /= div;
        }
        arr = smoothed;
    }
    return smoothed;
}

function smoothRectangular(arr, n, iter) {
    var smoothed = [];
    for (var it = 0; it < iter; it++) {
        for (var i = 0; i < arr.length; i++) {
            smoothed[i] = arr[i];
            var div = 1;
            for (var j = 1; j < ((n + 1) / 2); j++) {
                if ((i - j) < 0) {
                    smoothed[i] += arr[i + j];
                    div++;
                } else if ((i + j) >= arr.length) {
                    smoothed[i] += arr[i - j];
                    div++;
                } else {
                    smoothed[i] += arr[i - j] + arr[i + j];
                    div += 2;
                }    
            }
            smoothed[i] /= div;
        }
        arr = smoothed;
    }
    return smoothed;
}

